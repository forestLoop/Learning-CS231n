    # It's important to reshape it here, from (num_train,) to (num_train,1).
    margins = scores - correct_class_scores + 1
    margins[np.arange(num_train), y] = 0  # for the correct class, there should be no margin.
    loss = float(np.sum(margins[margins > 0]))
    loss = loss/num_train
    loss += reg * np.sum(W * W)

